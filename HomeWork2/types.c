#include <stdio.h>

int main()
{
    char myChar = 'a';
    int myInt = 42;
    float myFloat = 3.14;
    double myDouble = 3.14159265359;
    _Bool myBool = 1;
    printf("myChar: %c\n", myChar);
    printf("myInt: %d\n", myInt);
    printf("myFloat: %f\n", myFloat);
    printf("myDouble: %lf\n", myDouble);
    printf("myBool: %d\n", myBool);
    return 0;
}
